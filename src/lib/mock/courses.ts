// Helper function to generate lessons
const generateLessons = (courseId: string, lessonTitles: string[]): MockLesson[] => {
  return lessonTitles.map((title, index) => ({
    id: `${courseId}-lesson-${index + 1}`,
    title,
    description: `${title}的詳細說明與實作練習`,
    videoUrl: `https://example.com/video/${courseId}-${index + 1}.mp4`,
    videoDuration: 300 + Math.floor(Math.random() * 300), // 5-10 minutes
    order: index + 1,
  }));
};

