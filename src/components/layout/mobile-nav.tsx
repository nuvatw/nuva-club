import { useRole } from '@/lib/mock';
import { cn } from '@/lib/utils/cn';
import type { UserRole } from '@/types';

interface MobileNavProps {
  className?: string;
}

interface NavItem {
  href: string;
  label: string;
  icon: React.ReactNode;
}

const vavaMobileItems: NavItem[] = [
  { href: '/club/dashboard', label: '主頁', icon: <HomeIcon /> },
  { href: '/club/courses', label: '學習', icon: <BookIcon /> },
  { href: '/club/challenges', label: '挑戰', icon: <TrophyIcon /> },
  { href: '/club/messages', label: '訊息', icon: <MessageIcon /> },
];

const nunuMobileItems: NavItem[] = [
  { href: '/club/nunu/dashboard', label: '主頁', icon: <HomeIcon /> },
  { href: '/club/nunu/vavas', label: '學員', icon: <UsersIcon /> },
  { href: '/club/courses', label: '學習', icon: <BookIcon /> },
  { href: '/club/messages', label: '訊息', icon: <MessageIcon /> },
];

const guardianMobileItems: NavItem[] = [
  { href: '/club/guardian/dashboard', label: '主頁', icon: <HomeIcon /> },
  { href: '/club/guardian/users', label: '用戶', icon: <UsersIcon /> },
  { href: '/club/guardian/challenges', label: '內容', icon: <BookIcon /> },
  { href: '/club/messages', label: '訊息', icon: <MessageIcon /> },
];

function getMobileNavForRole(role: UserRole): NavItem[] {
  switch (role) {
    case 'guardian':
      return guardianMobileItems;
    case 'nunu':
      return nunuMobileItems;
    default:
      return vavaMobileItems;
  }
}

export function MobileNav({ className }: MobileNavProps) {
  const pathname = usePathname();
  const { role } = useRole();
  const navItems = getMobileNavForRole(role);

  return (
    <nav
      className={cn(
        'fixed bottom-0 left-0 right-0 z-50 border-t bg-white',
        className
      )}
    >
      <div className="flex h-16 items-center justify-around">
        {navItems.map((item) => {
          const isActive = pathname === item.href || pathname.startsWith(`${item.href}/`);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                'flex flex-col items-center gap-1 px-3 py-2',
                isActive ? 'text-primary' : 'text-muted-foreground'
              )}
            >
              <span className="h-5 w-5">{item.icon}</span>
              <span className="text-xs">{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}

